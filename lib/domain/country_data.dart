  final String nomeEn;
  final String nomePt;
  final String linkBandeira;
  final String capital;
  final String continente;
  final String totalPopulacao;
  final String cristaos;
  final String cristaosEvangelicos;
  final String maiorReligiao;
  final String religiaoMaisCresce;
